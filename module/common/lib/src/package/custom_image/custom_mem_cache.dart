/// The custom mem cache class for the custom image package.
final class CustomMemCache {
  /// The constructor for CustomMemCache.
  const CustomMemCache({this.memCacheHeight, this.memCacheWidth});

  /// The height of the image to be stored in the memory cache.
  final int? memCacheHeight;

  /// The width of the image to be stored in the memory cache.
  final int? memCacheWidth;
}
